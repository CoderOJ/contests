constexpr int MOD = 998244353;

int power(int a, int b)
{
  int r = 1;
  for (; b; b >>= 1)
  {
    if (b & 1)
      r = 1ll * r * a % MOD;
    a = 1ll * a * a % MOD;
  }
  return r;
}

int inv(int a)
{
  return power(a, MOD - 2);
}

constexpr int N = 1500;

std::pair<int,int> c_to_uv[128];

struct edge_t
{
  int u, v;
};

struct method_t
{
  int n;
  std::vector<edge_t> e;
  void parse(char *s)
  {
    if (*s == '\0') n = -1;
    else
      while (*s)
      {
        const auto& [u,v] = c_to_uv[(int)*s++];
        n = std::max({n, u + 1, v + 1});
        e.push_back({u, v});
      }
  }
};

method_t mt[N];
std::pair<int,int> mp[N * N];
bool mpt[N * N];
std::vector<std::pair<int,int>> mpv;

void output(int a, int b, int c, int d)
{
  printf("%d %lu\n", mt[a].n + mt[b].n + mt[c].n + mt[d].n,
                    mt[a].e.size() + mt[b].e.size() + mt[c].e.size() + mt[d].e.size() + 3);

  int pr = 1;
  for (const auto& [u, v]: mt[a].e) printf("%d %d\n", pr + u, pr + v);
  pr += mt[a].n;
  printf("%d %d\n", pr - 1, pr);
  for (const auto& [u, v]: mt[b].e) printf("%d %d\n", pr + u, pr + v);
  pr += mt[b].n;
  printf("%d %d\n", pr - 1, pr);
  for (const auto& [u, v]: mt[c].e) printf("%d %d\n", pr + u, pr + v);
  pr += mt[c].n;
  printf("%d %d\n", pr - 1, pr);
  for (const auto& [u, v]: mt[d].e) printf("%d %d\n", pr + u, pr + v);
}

